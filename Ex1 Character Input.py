# infomation
name = str(input('What is your name? '))
age = int(input('How old are you?' ))
year = int(input('Which year are you in?'))

# math and output
hundredage = int(100-age+year)
print('Your name is '+name.capitalize()+' and You are '+ str(age) + ' years old.')
print('You will turn 100 years old in '+str(hundredage)+'.')
